int
main(int argc, char * const *argv)
{
	int guid_alt = argc > 1 && !strcmp(argv[1], "-g");
	/* {2cb56191-ee6f-432d-a377-853d3c6b949e} */
	CSSM_GUID s3dl_guid =
	{
			0x2cb56191, 0xee6f, 0x432d,
			{ 0xa3, 0x77, 0x85, 0x3d, 0x3c, 0x6b, 0x94, 0x9e }
	};
	const CSSM_GUID *guid = guid_alt ? & s3dl_guid : &gGuidAppleFileDL;

	plan_tests(18);

	CSSM_DL_DB_HANDLE dldb = {};
	ok(cssm_attach(guid, &dldb.DLHandle), "cssm_attach");

	if (!tests_begin(argc, argv))
		return 255;

	const char *dbname = "test.keychain";
    ok_status(CSSM_DL_DbCreate(dldb.DLHandle, dbname, NULL /* DbLocation */,
                  &dbInfo,
                  CSSM_DB_ACCESS_READ | CSSM_DB_ACCESS_WRITE,
                  NULL /* CredAndAclEntry */,
                  NULL /* &openParameters */,
                  &dldb.DBHandle),
        "CSSM_DL_DbCreate pure dl");
	ok_status(CSSM_DL_DbClose(dldb), "close db");
	ok(cssm_detach(guid, dldb.DLHandle), "cssm_detach");

	SecKeychainRef keychain = NULL;
	ok_status(SecKeychainOpenWithGuid(guid, 0, CSSM_SERVICE_DL, dbname,
		NULL, &keychain), "Open pure dl keychain");
	ok(keychain, "keychain non NULL");

	certTests(keychain);

	return !tests_end(1);
}
